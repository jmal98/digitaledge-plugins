@Description("This configurable parser is used to parse XML files into a set of fields to be used by the translator.")
public class ConfigurableXMLParser extends AbstractXMLParser implements Initializable {
	
	/** For logging parsing problems. */
	private static final Logger log = Logger.getLogger(ConfigurableXMLParser.class);
		
	/** String which separates XPATH element nodes */
	private static final String elementNodeSeperator = "/";
	
	/** String which separates XPATH attribute nodes */
	private static final String attributeNodeSeperator = "/@";
	
	/** For translating to IRAD format. */
	private AbstractConfigurableTranslator translator;
	
	/** Helps transforming XML objects to and from Strings */
	private Transformer xmlTransformer;
	
	/** Source used if none is specified. */
	private String defaultSource;
	
	/** Access Label used if none is specified. */
	private String defaultAccessLabel;
	
	/** Defines the maximum number of elements allowed in the XPath node representation of the Map/JSon document */ 
	private int suffixPathDepth = 0;
	
	/** The policy to use when there is an error translating a parsed field */
	private String translatorFieldHandlingErrorPolicy;

	/**
	 * Default Constructor
	 */
	public ConfigurableXMLParser() {
		super();
	}
	
	/**
	 * Constructor that takes an xml file argument.
	 */
	public ConfigurableXMLParser(File xmlFile) {
		super(xmlFile);
	}
	
	/**
	 * Set the translator that will convert the parsed fields to a data model.
	 */
	public void setTranslator(AbstractConfigurableTranslator translator) {
		this.translator = translator;
	}
	
	/**
	 * @return the translator
	 */
	public AbstractConfigurableTranslator getTranslator() {
		return this.translator;
	}
	
	/**
	 * Set the default source string of the data. Can be overridden by the input stream parameters.
	 */
	@UserConfigured(value="UNKNOWN", description = "The string describing the source of the data.")
	public void setDefaultSource(String defaultSource) {
		this.defaultSource = defaultSource;
	}
	
	/**
	 * @return the default source
	 */
	public String getDefaultSource() {
		return this.defaultSource;
	}

	/**
	 * Set the default access label. Can be overridden by the input stream parameters or
	 * the translator.
	 */
	@UserConfigured(value="UNCLASSIFIED", description = "The default access label to include with the data.")
	public void setDefaultAccessLabel(String defaultAccessLabel) {
		this.defaultAccessLabel = defaultAccessLabel;
	}
	
	/**
	 * Gets the default access label.
	 * 
	 * @return A String containing the access label.
	 */
	public String getDefaultAccessLabel() {
		return this.defaultAccessLabel;
	}
	
	/**
	 * Get the SuffixPathDepth
	 * @return the maximum number of elements allowed in the XPath representation
	 */
	public int getSuffixPathDepth() {
		return suffixPathDepth;
	}

	/**
	 * Sets the SuffixPathDepth Depth.  This defaults to 0 which causes the parser to keep the full XPath representation
	 * for all elements.  By setting this to some value other than 0, the parser trims elements from the left of the 
	 * XPath representation until the path element count equals suffixPathDepthDepth.
	 * @param suffixPathDepth
	 */
	@UserConfigured(value = "0",
			description = "A number between 0 and 65 which is the maximum number of elements to keep in the XPath representation." +
						  "The default of 0 keeps the entire XPath, otherwise it equals the total number of rightmost elements to keep.", 
    		flexValidator = { "NumberValidator minValue=0 maxValue=65" })
	public void setSuffixPathDepth(int suffixPathDepth) {
		this.suffixPathDepth = suffixPathDepth;
	}
	
	/**
	 * Gets the policy used when an error occurs in translating a parsed field.  The default value is 
	 * @return the policy
	 */
	public String getFieldHandlingErrorPolicy() {
		return translatorFieldHandlingErrorPolicy;
	}
	
	/**
	 * Sets the policy used when an error occurs in translating a parsed field.  The default value is to log the error
	 * and discard the erroneous field.
	 * 
	 * @param translatorFieldHandlingErrorPolicy
	 */
	@UserConfigured(value = "DISCARD_AND_LOG",
					description = "The policy to use when an erroneous field is encountered in the parsing process.",
					flexValidator = { "RegExpValidator expression=(DISCARD_AND_LOG|DISCARD_AND_IGNORE)" } )
	public void setFieldHandlingErrorPolicy(String translatorFieldHandlingErrorPolicy) {
		this.translatorFieldHandlingErrorPolicy = translatorFieldHandlingErrorPolicy;
	}

	/**
	 * Perform disposal work for the parser.
	 */
	public void dispose() {
		translator.dispose();
	}

	/**
	 * Perform initialization work for the parser.
	 */
	public void initialize() throws InitializationException {
		translator.initialize();
		translator.setFieldHandlingErrorPolicy(translatorFieldHandlingErrorPolicy);
		try {
			xmlTransformer = TransformerFactory.newInstance().newTransformer();
			xmlTransformer.setOutputProperty(javax.xml.transform.OutputKeys.OMIT_XML_DECLARATION, "yes");
			xmlTransformer.setOutputProperty(javax.xml.transform.OutputKeys.INDENT, "no");
		} catch (TransformerConfigurationException e) {
			log.fatal("Unable to configure xml transformer.");
		} catch (TransformerFactoryConfigurationError e) {
			log.fatal("Unable to configure xml transformer.");
		}
	}
	
	/**
     * Parse and return the next JSON record, or null if all done or error.
     * 
     * @return
     * 			Returns the parsed JSONObject.
     */
	public JSONObject parse() throws ParsePipelineException {
		
		JSONObject parsedData = null;
		
		String streamAccessLabel = info.getProperty(StandardHeader.ACCESS_LABEL_KEY);
		String accessLabel = (streamAccessLabel == null) ? defaultAccessLabel : streamAccessLabel;
		
		String streamSource = info.getProperty(StandardHeader.SOURCE_KEY);
		String source = (streamSource == null) ? defaultSource : streamSource;
		
		HashMap<String, String> map = new HashMap<String, String>();
		
		try {
			Document recordDoc = nextRecordAsDocument();
			if (recordDoc == null) { 
				return null;
			}
			Element recordElement = recordDoc.getDocumentElement();
			loadXMLData(map, recordElement);
			if (this.suffixPathDepth > 0) {
				map = trimXPathRepresentation(map);
			}
			parsedData = translator.recordTranslation( map, source, accessLabel );
		} catch (Exception ex) {
			log.error("XML Parsing Error: " + ex.getMessage());
			ParseErrorCounter.instance.increment();
			throw new ParsePipelineException(ex.getMessage(), ex);
		} 
		
		return parsedData;
	}
	
	/**
	 * This method will be called recursively to process all the variables.  It will process both element nodes and 
	 * attribute nodes.  It will store these variables in a map using the XPATH name of the variable.
	 * 
	 * @param map
	 * 				HashMap that contains the data that will be used to build the IRAD JSON file.
	 * @param node
	 * 				Name of the start node.
	 */
	
	private void loadXMLData(HashMap<String, String> map, Node node) {
		int index = index(node);
		if (index >= 0) {
			// there is a sequence of this element, make a JSONArray out of this element's content
			String xPathName = determineXPathName(node) + "[" + index + "]";
			String content = XMLtoJSON(serializeXML(node));
			map.put(xPathName, content);
			return;
		}

		if (node.hasAttributes()) {
			NamedNodeMap attributeMap = node.getAttributes();
			for (int j=0; j < attributeMap.getLength(); j++) {
				Node attributeNode = attributeMap.item(j);
				String xPathName = determineXPathName(node) + attributeNodeSeperator + attributeNode.getNodeName();
				String content = attributeNode.getTextContent().trim();
				map.put(xPathName, content);
			}
		}

		NodeList nodeList = node.getChildNodes();  
		int listLength = nodeList.getLength();
		for (int i = 0; i < listLength; i++) {
			Node currentNode = nodeList.item(i);
			if (currentNode.getNodeType() == Node.ELEMENT_NODE) {
				Node nextNode = currentNode.getFirstChild();
				if (nextNode != null) {
					if (nextNode.getNodeType() == Node.TEXT_NODE )	{
						String xPathName = determineXPathName(currentNode);
						String content = nextNode.getTextContent().trim();
						map.put(xPathName, content);
					}
				}
				loadXMLData(map, currentNode);
			}
		}
	}
	
	private String serializeXML(Node node) {
		java.io.StringWriter sw = new java.io.StringWriter();
		try {
			// wow what a hack, here
			NodeList l = node.getChildNodes();
			if ( ! node.hasAttributes() &&
					node.getNodeType() == Node.ELEMENT_NODE &&
					l.getLength() == 1 &&
					l.item(0).getNodeType() == Node.TEXT_NODE) {
				Element e = (Element) node;
				e.setAttribute("REMOVEME", "");
			}
			xmlTransformer.transform(new DOMSource(node), new StreamResult(sw));
		} catch (TransformerException e) {
			log.error("Unable to execute xml transformer.");
		}
		return sw.toString();
	}
	
	private String XMLtoJSON(String xml) {
		XMLSerializer serializer = new XMLSerializer();
		serializer.setTrimSpaces(true);
		String json = serializer.read(xml).toString();
		// by default attributes are preceeded with '@', so remove them
		// but allow '@' in the actual data
		json = json.replaceAll("\\{\"@", "{\"");
		json = json.replaceAll(",\"@", ",\"");
		json = json.replace("\"REMOVEME\":\"\",", "");
		return json;
	}
	
	/**
	 * Trims the XPath elements keys of this map to be no longer than suffixPathDepth.  Does this by removing elements
	 * starting from the left of the XPath String.
	 * @param map The Map of XPath keys to element/attribute values
	 * @return A copy of the original map with all XPath keys trimmed to suffixPathDepth
	 */
	private HashMap<String, String> trimXPathRepresentation(HashMap<String, String> map) {
		HashMap<String, String> trimmedMap = new HashMap<String, String>(map.size());
		Iterator<Map.Entry<String, String>> entries = map.entrySet().iterator();
		while (entries.hasNext()) {
		    Map.Entry<String, String> entry = entries.next();
		    String origKey = entry.getKey();
		    String[] splitKey = origKey.split("\\/");
		    
		    int startIndex = (splitKey.length > this.suffixPathDepth ? splitKey.length - this.suffixPathDepth: 1);
			StringBuilder trimmedKeySB = new StringBuilder();
			for (int i = startIndex; i < splitKey.length; i++) {
				trimmedKeySB.append("/").append(splitKey[i]);
			}
			String trimmedKey = trimmedKeySB.toString();
			if (trimmedMap.containsKey(trimmedKey)) {
				log.warn("The suffixPathDepth of:" + suffixPathDepth + " resulted in a duplicate key:" + trimmedKey);
				log.warn("This will result in data loss, increase your suffixPathDepth to ensure unique XPath key names.");
			}
			trimmedMap.put(trimmedKey.toString(), entry.getValue());
		}
		return trimmedMap;
	}
	
	/**
	 * 
	 * Returns the XPATH name of the input Node. 
	 * 
	 * @param node
	 * 				The ending node for determining the XPATH name.
	 * @return
	 * 				The XPATH Name of the variable.
	 */
	
	private String determineXPathName(Node node) {
		StringBuffer  xPathName = new StringBuffer();
		if (node != null) {
			if (node.getNodeType() == Node.ELEMENT_NODE) {
				xPathName.append(elementNodeSeperator  + node.getNodeName());
			} else if(node.getNodeType() == Node.ATTRIBUTE_NODE) {
				xPathName.append(attributeNodeSeperator + node.getNodeName());
			} 
			
			Node parentNode;
			while((parentNode = node.getParentNode()) != null) {
				if (parentNode.getNodeType() == Node.ELEMENT_NODE) {
					xPathName.insert(0, "/" + parentNode.getNodeName());
				}
				node = parentNode;
			}
		}
		return xPathName.toString();
	}

}
