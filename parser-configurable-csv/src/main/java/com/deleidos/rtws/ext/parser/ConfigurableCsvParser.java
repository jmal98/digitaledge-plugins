	public void setStopOnMissingField(boolean stopOnMissingField) {
		this.stopOnMissingField = stopOnMissingField;
	}
	
	/**
	 * Set the quoted record field
	 */
	@UserConfigured(value="false",
					description = "Specifies whether or not each record is wrapped in quotes.",
					flexValidator={"RegExpValidator expression=^(true|false)$"})
	public void setQuotedRecord(boolean quotedRecord) {
		this.quotedRecord = quotedRecord;
	}
	
	/**
	 * @return the stop-on-missing field
	 */
	@NotNull
	public boolean getStopOnMissingField() {
		return this.stopOnMissingField;
	}
	
	/**
	 * Set the mode field
	 */
	@UserConfigured(value="Parse",
					description = "Determines the mode in which the parser will run.",
					flexValidator={"RegExpValidator expression=^(Parse|Metrics)$"})
	public void setMode(String mode) {
		this.mode = Mode.fromValue(mode);
	}
	
	/**
	 * @return the mode field
	 */
	public String getMode() {
		return mode.toString();
	}
	
	/**
	 * Perform initialization work on the parser.
	 */
	public void initialize() throws InitializationException {
    	translator.initialize();
    }
	
	/**
	 * Perform disposal work on the parser.
	 */
	public void dispose() {
    	translator.dispose();
    }

    /**
     * Parse and return the next JSON record, or null if all done or error.
     */
	public JSONObject parse() throws ParsePipelineException {
		
		if (headerKeys == null) {
			log.error("ConfigurableCsvParser: No header keys found (perhaps an error during header processing?)");
			return null;
		}
		
		currentRecord = splitter.split();
		if (currentRecord == null) 
			return null;
		
		String streamAccessLabel = info.getProperty(StandardHeader.ACCESS_LABEL_KEY);
		String accessLabel = (streamAccessLabel == null) ? defaultAccessLabel : streamAccessLabel;
		
		String streamSource = info.getProperty(StandardHeader.SOURCE_KEY);
		String source = (streamSource == null) ? defaultSource : streamSource;
		
		if (quotedRecord) {
			currentRecord = currentRecord.substring(1,currentRecord.length() - 1);
		}

		if(mode.equals(Mode.PARSE)) {
			try {
				String[] parsedData = CSVParser.parse(currentRecord, 0, delimiter);
				if (parsedData == null)
					return null;
				
				// Place the data as it is in a the map
				HashMap<String, String> map = new HashMap<String, String>();
				for (int i=0; i<parsedData.length; i++) {
					map.put(headerKeys[i], parsedData[i]);
				}

				return translator.recordTranslation( map, source, accessLabel );
			} catch (ParseException e) {
				log.error("Error parsing record in ConfigurableCsvParser: " + e.getMessage());
				ParseErrorCounter.instance.increment();
				throw new ParsePipelineException(e.getMessage(), e);
			}  catch (ArrayIndexOutOfBoundsException e ) {
				log.error("Error parsing record in ConfigurableCsvParser: " + e.getMessage());
				ParseErrorCounter.instance.increment();
				throw new ParsePipelineException(e.getMessage(), e);
			}
		}
		else if(mode.equals(Mode.METRICS)) {
			// The source field should be the filename
			
			try {
				// Make sure the MetricsMap exists for the filename
				if(!fileToMetricsMap.containsKey(source)) {
					fileToMetricsMap.put(source, generateEmptyMetricsMap());
				}
				MetricsMap metrics = fileToMetricsMap.get(source);
				
				// Perform different metrics tests
				do {
					ArrayList<String> errorMessages = new ArrayList<String>();
					
					metrics.increment(CSVParserMetricsKey.TOTAL_RECORD_COUNT);
					
					try {
						String[] parsedData = CSVParser.parse(currentRecord, 0, delimiter);
								
						// Field count mismatches with the file header
						int mismatchCount = headerKeys.length - parsedData.length;
						if(mismatchCount > 0) {
							metrics.increment(CSVParserMetricsKey.INCORRECT_FIELD_TO_COLUMN_COUNT_ERROR_COUNT);
							errorMessages.add(String.format("Record has %d less fields than column headers.", mismatchCount));
						}
						
						// Metrics on the fields
						ArrayList<String> emptyFieldHeaderKeys = new ArrayList<String>();
						ArrayList<String> nonAsciiFields = new ArrayList<String>();
						for(int i = 0; i < parsedData.length; i++) {
							String field = parsedData[i];
							
							//  Empty fields
							if(field.isEmpty()) {
								// Save the column key that contains the empty field
								emptyFieldHeaderKeys.add(headerKeys[i]);
							}
								
							// Fields with non-ASCII characters
							if(!containsOnlyAscii(field)) {
								nonAsciiFields.add(field);
							}
							
						}
						if(emptyFieldHeaderKeys.size() > 0) {
							metrics.increment(CSVParserMetricsKey.EMPTY_FIELD_ERROR_COUNT);
							String errorString = emptyFieldHeaderKeys.toString();
							errorString = errorString.substring(1, errorString.length()-1);	// Remove the "[" & "]" brackets from the array
							errorMessages.add(String.format("Record contains %d empty fields. Missing fields for the headers: %s.",
																emptyFieldHeaderKeys.size(), errorString));
						}
						if(nonAsciiFields.size() > 0) {
							metrics.increment(CSVParserMetricsKey.NON_ASCII_ERROR_COUNT);
							String errorString = nonAsciiFields.toString();
							errorString = errorString.substring(1, errorString.length()-1);	// Remove the "[" & "]" brackets from the array
							errorMessages.add(String.format("Record contains %d fields with non-ASCII characters: %s.",
																nonAsciiFields.size(), errorString));
						}
					} catch (ParseException e) {
						// Errors parsing the input record using the DigitalEdge CSVParser package.
						metrics.increment(CSVParserMetricsKey.CSV_PARSER_ERROR_COUNT);
						errorMessages.add(String.format("CSV Parser failed to parse record: %s.", e.getMessage()));
					}
					
					if(errorMessages.size() > 0) {
						return translator.recordTranslation(
												buildMetricsErrorRecordMap(source, currentRecord, errorMessages),
												source,
												accessLabel);
					}
					else {
						// Get the next record to process
						currentRecord = splitter.split();
					}
				} while ( (currentRecord != null) );
				
				// We've reached the end of the input stream, return the accumulated metrics without an error
				return translator.recordTranslation(
										buildMetricsRecordMap(source),
										source,
										accessLabel);
			} catch (ParseException e) {
				log.error("Error parsing record in ConfigurableCsvParser: " + e.getMessage());
				throw new ParsePipelineException(e.getMessage(), e);
			}
		}
		else {
			// Unknown mode: should be unreachable due to configuration of setMode() method
			log.error("Unknown parse mode: " + mode.toString());
			throw new ParsePipelineException("Unknown parse mode: " + mode.toString());
		}
	}
	
	/**
	 * Determine the header key, i.e., the ordered list of field names in the input data.
	 * If the header key filename property was set, then grab the header keys from that.
	 * Otherwise, assume the first line of the stream contains the header keys.
	 */
	public void parseHeaders() {
		try {
			if (headerKeyFilename == null) {
				// No header key file property set - assume the first line is a header key
				String headerRow = splitter.split();
				this.headerKeys = CSVParser.parse(headerRow, 0, delimiter);
				//this.headerKeys = headerRow.split(delimiter+"");//nextRecord(delimiter);			
			}
			else {
	    		String headerKeyLine = FileUtils.readFileToString(new File(headerKeyFilename));
	    		this.headerKeys = CSVParser.parse(headerKeyLine, 0, delimiter);//headerKeyLine.split(delimiter + "");
			}			
			translator.validateHeaders(headerKeys, stopOnMissingField);
		}
		catch (IOException e) {
			log.error("IO Error in parseHeaders: ", e );
			headerKeys = null;
		}
		catch (ParseException e) {
			log.error("ParseException in parseHeaders: ", e );
			headerKeys = null;
		}
		catch (ValidationException e) {
			// Missing Fields are already logged
			log.error(e.getMessage());
			headerKeys = null;
		} 
	}
	
