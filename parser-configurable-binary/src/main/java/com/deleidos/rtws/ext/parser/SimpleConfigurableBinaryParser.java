	public void setCustomConfig(CustomConfig cfg) {
		configuration = cfg;
	}

	/**
	 * @return the field file
	 */
	public CustomConfig getCustomConfig() {
		return configuration;
	}

	/**
	 * Set the default source.
	 */
	@UserConfigured(value="UNKNOWN", description = "The string describing the source of the data.")
	public void setDefaultSource(String defaultSource) {
		this.defaultSource = defaultSource;
	}

	/**
	 * @return the default source
	 */
	public String getDefaultSource() {
		return defaultSource;
	}

	private abstract class Field {

		public static final String definitions = "Should contain one or more of:\n"
				+ "READTOEND:fieldName:startPos:fieldType\n"
				+ "FIXED:fieldName:startPos:length:fieldType:reverse\n"
				+ "REGEX:fieldName:regex:targetField,fieldType\n"
				+ "BACKREFERENCE:fieldName:startPos:otherField:fieldType:reverse\n"
				+ "Where fieldtype can be any of 'ASCII', 'NUMBER', or 'BYTES'\n"
				+ "The reverse flag (can take the form 'true', 'reverse', 'r', 't') tells the parser to reverse byte order.\n"
				+ "Backreference and Regex fields must occur in the file after the referenced field.";

		protected String fieldName;
		protected infoType type;

		public abstract int[] readField(byte[] bytes,
				Map<String, String> readFields);

		public infoType getType() {
			return type;
		}

		public String getFieldName() {
			return fieldName;
		}
	}

	private abstract class ReversableField extends Field {
		protected String[] reverseValues = { "true", "reverse", "r", "t" };
		protected boolean reverseByteOrder;

		protected void setReverseOrder(String s) {
			for (int i = 0; i < reverseValues.length && !reverseByteOrder; i++)
				if (s.equalsIgnoreCase(reverseValues[i]))
					reverseByteOrder = true;
		}
	}

	private enum FieldTypes {
		READTOEND, FIXED, REGEX, BACKREFERENCE
	}

	private class readToEndField extends Field {

		private int startPos;

		@Override
		public int[] readField(byte[] arr, Map<String, String> notUsed) {
			int[] out = new int[arr.length - startPos];
			for (int i = startPos; i < arr.length; i++)
				out[i - startPos] = (int) arr[i] & 0xff;
			return out;
		}

		public readToEndField(String fName, int startPosition, infoType fType) {
			startPos = startPosition;
			fieldName = fName;
			type = fType;
		}
	}

	private class fixedField extends ReversableField {
		private int startpos;
		private int len;

		@Override
		public int[] readField(byte[] arr, Map<String, String> readFields) {
			// start at startpos, end at startpos+len-1 (inclusive), unless byte
			// order reversed

			int start = reverseByteOrder ? startpos + len - 1 : startpos;
			int end = reverseByteOrder ? startpos - 1 : startpos + len;
			int change = reverseByteOrder ? -1 : 1;
			int[] data = new int[len];
			int dataindex = 0;
			for (int i = start; i != end; i += change) {
				data[dataindex] = (int) arr[i] & 0xff;
				dataindex++;
			}
			return data;
		}

		public fixedField(String fName, int startPosition, int length,
				infoType fType, String strRev) {
			fieldName = fName;
			startpos = startPosition;
			len = length;
			type = fType;
			setReverseOrder(strRev);
		}

	}

	private class regexField extends Field {
		private String expression;
		private String fieldToApply;
		private Pattern regExPattern;
		private Matcher regExMatch;

		public regexField(String fName, String regex, String targetField,
				infoType fType) {
			fieldName = fName;
			type = fType;
			expression = regex;
			fieldToApply = targetField;
			regExPattern = Pattern.compile(expression);
			regExMatch = regExPattern.matcher("");

		}

		public int[] readField(byte[] arr, Map<String, String> readFields) {
			int[] data = null;
			try {
				String matchOn = readFields.get(fieldToApply);
				regExMatch.reset(matchOn);
				regExMatch.find();
				String retData = regExMatch.group(regExMatch.groupCount());
				byte[] bDat = retData.getBytes();
				data = new int[bDat.length];
				for (int i = 0; i < bDat.length; i++)
					data[i] = bDat[i];
			} catch (Exception e) {
				log.debug("Could not match regex " + regExPattern.pattern()
						+ " in string " + readFields.get(fieldToApply)
						+ ".  No value for this field.", e);
				data = null;
			}
			return data;
		}
	}

	private class backReferenceField extends ReversableField {
		// this class uses a parsed length from a previous field
		private int startPos;
		private String lengthField;

		public backReferenceField(String fName, int start, String lField,
				infoType fType, String strRev) {
			fieldName = fName;
			type = fType;
			startPos = start;
			lengthField = lField;
			setReverseOrder(strRev);
		}

		public int[] readField(byte[] arr, Map<String, String> readFields) {
			fixedField inner;
			int len = Integer.parseInt(readFields.get(lengthField));
			inner = new fixedField(fieldName, startPos, len, type,
					((Boolean) reverseByteOrder).toString());
			return inner.readField(arr, readFields);
		}

	}

	private enum infoType {
		ASCII, NUMBER, BYTES
	}

