	public void setDefaultAccessLabel(String defaultAccessLabel) {
		this.defaultAccessLabel = defaultAccessLabel;
	}

	/**
	 * Is this data base64 encoded?
	 */
	@UserConfigured(value="false", description = "Set if this data is base64 encoded.")
	public void setBase64Encoded(boolean encoded) {
		this.base64Encoded = encoded;
	}
	
	/**
	 * Is this data base64 encoded?
	 */
	public boolean getBase64Encoded() {
		return this.base64Encoded;
	}

	
