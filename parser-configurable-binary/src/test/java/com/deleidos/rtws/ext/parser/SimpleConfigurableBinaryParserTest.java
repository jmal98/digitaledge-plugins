@Ignore("disable until dependent model has been re-created.")
public class SimpleConfigurableBinaryParserTest {

	private SimpleConfigurableBinaryParser binaryParser;
	private SimpleConfigurableTranslator translator;
	private static String basedir;
	
	
	@BeforeClass
	public static void init() {

		basedir = System.getProperty("basedir");
		if (basedir == null) {
			System.err
					.println("Need to set basedir in system properties at startup time");
		}
		Properties testProperties = System.getProperties();

		testProperties.setProperty("RTWS_TENANT_ID", "aws-dev");
		testProperties.setProperty("password", "redacted");
		testProperties.setProperty("RTWS_BUCKET_NAME", "test-bucket");
		testProperties.setProperty("RTWS_DOMAIN", "nothing.rtsaic.com");
		testProperties.setProperty("RTWS_MOUNT_MODE", "s3cmd");
		testProperties.setProperty("RTWS_MAX_ALLOCATION_REQUEST", "");
		testProperties.setProperty("RTWS_TEST_MODE", "false");
		System.setProperties(testProperties);
	}

	@Before
	public void setUp() throws Exception {
		binaryParser = new SimpleConfigurableBinaryParser();
		binaryParser.setDefaultSource("JUNIT1");
		binaryParser.setDefaultAccessLabel("UNCLASSIFIED");
		basedir = System.getProperty("basedir");
		if (basedir == null) {
			System.err.println("Need to set basedir in system properties at startup time");
		}

		Properties emptyProps = new Properties();
		binaryParser.setStreamProperties(emptyProps);
		translator = new SimpleConfigurableTranslator();
		translator.setModelName("Zions");
		translator.setModelVersion("1.0");
		translator.setInputFormatName("zionsbinary");
		binaryParser.setDefaultSource("zionsbinary");
		binaryParser.setTranslator(translator);
	}

	@After
	public void tearDown() throws Exception {
		binaryParser.dispose();
	}

	@Test
	public void testInitialize() {
		binaryParser.initialize();
	}

	@Test
	public void testGettersSetters() throws Exception {

		binaryParser.initialize();
		binaryParser.setInputStream(new StringInputStream(
				createTransactionUDPPacket()));
		binaryParser.parseHeaders();

		AbstractConfigurableTranslator translator = binaryParser
				.getTranslator();
		assertNotNull(translator);


		
		assertTrue("UNCLASSIFIED".equalsIgnoreCase(binaryParser
				.getDefaultAccessLabel()));

	}

	@SuppressWarnings("unchecked")
	@Test
	public void testParse() throws Exception {

		binaryParser.initialize();
		binaryParser.setInputStream(new StringInputStream(
				createTransactionUDPPacket()));
		binaryParser.parseHeaders();

		assertNotNull(binaryParser.getFields());
		assertTrue(binaryParser.getFields().size() > 0);
		JSONObject jsonObj;
		jsonObj = binaryParser.parse();
		assertNotNull(jsonObj);
		assertTrue(jsonObj.getString("Bank").equalsIgnoreCase("001"));
		JSONObject headerObj = jsonObj.getJSONObject("standardHeader");
		assertNotNull(headerObj);
		assertTrue(headerObj.getString(StandardHeader.ACCESS_LABEL_KEY).equals(
				"UNCLASSIFIED"));
		assertTrue(headerObj.getString(StandardHeader.SOURCE_KEY).equals(
				"zionsbinary"));
		Collection<Object> c = jsonObj.values();
		assertEquals(9,c.size());

		// Verify that properties can be set to change source and access label
		Properties props = new Properties();
		props.put(StandardHeader.ACCESS_LABEL_KEY, "FOR GREEN EYES ONLY");
		props.put(StandardHeader.SOURCE_KEY, "zions");
		binaryParser.setStreamProperties(props);
		binaryParser.setInputStream(new StringInputStream(
				createTransactionUDPPacket()));
		binaryParser.parseHeaders();
		jsonObj = binaryParser.parse();
		assertNotNull(jsonObj);

		headerObj = jsonObj.getJSONObject("standardHeader");
		assertNotNull(headerObj);
		assertTrue(headerObj.getString(StandardHeader.ACCESS_LABEL_KEY).equals(
				"FOR GREEN EYES ONLY"));
		assertTrue(headerObj.getString(StandardHeader.SOURCE_KEY).equals(
				"zions"));

	}
	
	@Test
	public void testTranslatorHasHeaderNames() throws Exception {

		binaryParser.initialize();
		binaryParser.setInputStream(new StringInputStream(
				createTransactionUDPPacket()));
		binaryParser.parseHeaders();
		AbstractConfigurableTranslator abstractConfigurableTranslator = binaryParser.getTranslator();
		List<String> headers = abstractConfigurableTranslator.getExpectedHeaderNames();
		assertTrue(headers.contains("Bank"));
		assertTrue(headers.contains("ProductCode"));
		assertTrue(headers.contains("Account"));
		assertTrue(headers.contains("TellerID"));
		assertTrue(headers.contains("BranchID"));
		assertTrue(headers.contains("TransactionType"));
		assertTrue(headers.contains("DateStamp"));
	}

	private static String createTransactionUDPPacket() {
		Random rand = new Random();

		String[] banks = { "001"};
		String[] transTypes = {"1010","1510","1750"};
		String[] prodTypes = {"01","03","04","08","22","23"};

		String bank = banks[rand.nextInt(banks.length)];
		String teller =  ""+rand.nextInt(100000);
		while(teller.length()<5){
			teller = "0"+teller;
		}
		String branch = ""+rand.nextInt(100000);
		while(branch.length()<5){
			branch = "0"+branch;
		}
		String transactionType = transTypes[rand.nextInt(transTypes.length)];
		String product = prodTypes[rand.nextInt(prodTypes.length)];
		String account="";
		for(int i=0;i<10;i++)
			account = rand.nextInt(10)+account;

		String transStr = "'A<TL01TELR0E"+bank+teller+"12345"+branch+"121211091201"+transactionType+"000000021107500000000000            02000.................."+product+"."+account+".56432135135406354103540";
		
		System.out.println("Before Encoding: " + transStr);
		
		String base64Encodedpkt = javax.xml.bind.DatatypeConverter.printBase64Binary(transStr.getBytes());
		return base64Encodedpkt;
	}
	
	public static void main(String [] args) {
		
		SimpleConfigurableBinaryParserTest test = new SimpleConfigurableBinaryParserTest();
		System.out.println("UDP Packet: " + test.createTransactionUDPPacket());
		
		
	}
}
