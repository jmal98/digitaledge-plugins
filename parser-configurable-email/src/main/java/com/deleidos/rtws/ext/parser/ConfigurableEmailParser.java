	/**
     * Parse and return the the email as a JSON record, or error.
     */
	@SuppressWarnings("unchecked")
	public JSONObject parse() throws ParsePipelineException {
		
		JSONObject result = null;
		MimeMessage message = null;
		String streamAccessLabel = info.getProperty(StandardHeader.ACCESS_LABEL_KEY);
		String accessLabel = (streamAccessLabel == null) ? defaultAccessLabel : streamAccessLabel;
	
		String streamSource = info.getProperty(StandardHeader.SOURCE_KEY);
		String source = (streamSource == null) ? defaultSource : streamSource;

		HashMap <String, String> emailDataMap = new HashMap<String, String>();
		
		try {
		// Get the Email Message
			Properties props = System.getProperties();
			Session session = Session.getInstance(props);			
			// TODO don't know best way to get "currentRecord"
			currentRecord = "";
			message = getEmailMessage(session);
		} catch (MessagingException me) {
			log.error("Failed to get the email off the queue: " + me);
			throw new ParsePipelineException(me.getMessage(), me);
		}
		if (message != null) {
			try {
				Address from[] = message.getFrom();
				String  msgId  = message.getMessageID();

				// The MimeMessage parser is very permissive, parsing just about anything
				// you throw at it without error. Ensure that at least the From and Message-ID
				// headers are present to filter out non-email data
				
				if (from == null)
					log.error("Ignoring an email message without a From header");
				else if (msgId == null)
					log.error("Ignoring an email message without a Message-ID header");
				else {						
					// Get Email Message Header
					Enumeration<Header> headerData = message.getAllHeaders();
					
					// Place Header Fields into a Map 
					loadEmailHeaderFields(emailDataMap, headerData);	
							
					// Place the raw content of the message into the map
					loadRawMessageContent(message, emailDataMap);
											
					// Get the body of the message and add to the Map
					loadBodyPart(message, message, emailDataMap);
					
					result = translator.recordTranslation(emailDataMap, source, accessLabel);
				}		
			} catch(ParseException pe){
				log.error("Error parsing record in ConfigurableEmailParser: " + pe.getMessage());
				throw new ParsePipelineException(pe.getMessage(), pe);
			} catch (MessagingException me) {
				log.error("Failed to parse the email: " + me);
				throw new ParsePipelineException(me.getMessage(), me);
			} catch (IOException ioe) {
				log.error("Failed to parse the email: " + ioe);
				throw new ParsePipelineException(ioe.getMessage(), ioe);
			}
		}
		
		return result;
	}
	
	private MimeMessage getEmailMessage(Session session) throws MessagingException {
		
		MimeMessage message = null;
		Boolean dataAvailable = false;

		try {
			int count = input.available();
			dataAvailable = (count > 0);
		}
		catch (IOException e) {
			dataAvailable = false;
		}
		
		if (dataAvailable) {
			message = new MimeMessage(session, input);
			
			// If the "email" is a URL, MimeMessage will parse it successfully and treat the
			// http part as a header key, and the rest of the URL as its value. Here we test
			// for that.
			String httpHeader[] = message.getHeader("http");
			if ((httpHeader != null) && (message.getFrom() == null) && (message.getMessageID() == null)) {
				String url = "http:" + httpHeader[0];
				message = getEmailMessageFromStorageServer(url, session);
				// If we got the message from the storage server then remove it from the storage server
				if (message != null) {
					new EmailAttachmentLoader(getStorage()).removeEmailMessageFromStorageServer(url);
				}
			}
		}
		
		return message;
	}
		
	private MimeMessage getEmailMessageFromStorageServer(String uriString, Session session) {
		
		MimeMessage emailMessage = null;
		HttpClient client = new HttpClient();
		GetMethod fileGet = new GetMethod(uriString);
				
		try {
			String responseString ="";
			int status = client.executeMethod(fileGet);
			 
			if (status == HttpStatus.SC_OK) {
				ByteArrayInputStream bais = new ByteArrayInputStream(fileGet.getResponseBody());
				emailMessage = new MimeMessage(session, bais);
	        } else {
	        	responseString = HttpStatus.getStatusText(status); 
	        	log.error("Error Downloading File: " + responseString);
	        }
		} catch (IOException ioe) {
			log.error("Error Downloading File: " + ioe.getMessage());
			return emailMessage;
		} catch (Exception ex) {
			log.error("Error: " + ex.getMessage());
			return emailMessage;
		} finally {
			fileGet.releaseConnection();
		}
		
		return emailMessage;
	}
		
	
	private void loadEmailHeaderFields(HashMap<String, String> map, Enumeration<Header> headerData)	{
		
		while (headerData.hasMoreElements()) {
			
			Header headerElement = (Header) headerData.nextElement();
			// Check if this record exists in the map already
			String headerField = headerElement.getName().toLowerCase();
			
			if (map.containsKey(headerField)) {
				// Going to store all occurrences of the header field that the email contains 
				String newValue = map.get(headerField) + delimiter;
				newValue+=headerElement.getValue();
				map.put(headerField, newValue);
			}
			else
				map.put(headerField, headerElement.getValue());
		}
	}
		
	private void loadRawMessageContent(MimeMessage message, HashMap <String, String> emailDataMap)
	{
		try {
			byte[] bytes = new byte[message.getInputStream().available()];
			message.getInputStream().read(bytes);
			String content = new String(bytes);
			emailDataMap.put(RAW_BODY_NAME, content + FILE_SEPARATOR);
			
		} catch (Exception ioe) {
			log.error("Failed to get the Raw version of the Message Content: " + ioe);
		}
	}
	
	@SuppressWarnings("unchecked")
	private void loadBodyPart(MimeMessage message, Part bodyPart, HashMap <String, String> emailDataMap) throws MessagingException, IOException {

		if (bodyPart.isMimeType("text/*")) {
			Object bodyContent = bodyPart.getContent();
			
			if (bodyContent instanceof String)
				loadEmailProcessedBody(emailDataMap, (String)bodyPart.getContent());
			else if (bodyContent instanceof InputStream)
				// Not sure yet if we need this case
				loadEmailProcessedBody(emailDataMap, "Email Body Contains Content of InputStream");
			else {
				String bodyPartEncoding = ((MimeBodyPart)bodyPart).getEncoding();
				if (bodyPartEncoding != null) {
					InputStream is = MimeUtility.decode(message.getInputStream(), bodyPartEncoding);
					byte[] bytes = new byte[is.available()];
					is.read(bytes);
					String content = new String(bytes);
					loadEmailProcessedBody(emailDataMap, content);
				}
			}
		}
		else if (bodyPart.isMimeType("multipart/*")) {
			Multipart mp = (Multipart)bodyPart.getContent();
			int count = mp.getCount();
			
		    for (int i = 0; i < count; i++) {
		    	loadBodyPart(message, (Part)mp.getBodyPart(i), emailDataMap);
		    }
		}
		else if (bodyPart.isMimeType("message/*")) {
			// This is a Nested Message
			Message nestedMsg = (Message)bodyPart.getContent();
			loadEmailHeaderFields(emailDataMap, nestedMsg.getAllHeaders());
			loadBodyPart(message, nestedMsg, emailDataMap);
		}
		
		if (!bodyPart.isMimeType("multipart/*")) {
		    String disp = bodyPart.getDisposition();
		    // many mailers don't include a Content-Disposition so we need to include null here
		    if (disp == null || disp.equalsIgnoreCase(Part.ATTACHMENT) || disp.equalsIgnoreCase(Part.INLINE)) {
		    	String filename = bodyPart.getFileName();
		    	if (filename != null) {
		    		try {
	    				File source = new File(filename);
					    ((MimeBodyPart)bodyPart).saveFile(source);
					    String content = ((MimeBodyPart)bodyPart).getContentType();
					    String contentType = content.substring(0,content.lastIndexOf(';'));
					    String fileEncoding = ((MimeBodyPart)bodyPart).getEncoding();
					    saveEmailAttachment(emailDataMap, source, contentType, fileEncoding);
					} catch (IOException ex) {
						log.error("Failed to save attachment: " + ex);
					}
		    	}
		    }
		}
	}
	
	private void saveEmailAttachment(HashMap<String, String> map, File filename, String contentType, String fileEncoding) {
		
		log.info("Need to save the attached/inline file" + filename.getName());
		
		// Add attachment to the S3 content store and get the location URL
		String url = "";
		try {
			url = new EmailAttachmentLoader(getStorage()).addAttachmentToStore(emailAttachmentBucketName, filename, contentType, fileEncoding);
		} catch (Exception ex) {
			log.error("Unable to store the email attachment.", ex);
		}
		
		if (map.containsKey(ATTACHMENT_KEY)) {
			// Going to store all occurrences of the header field that the email contains 
			String newValue = map.get(ATTACHMENT_KEY) + delimiter;
			newValue+=url;
			map.put(ATTACHMENT_KEY, newValue);
		}
		else
			map.put(ATTACHMENT_KEY, url);
	}
	
	private void loadEmailProcessedBody(HashMap<String, String> map, String emailBody)
	{
		// Check if this record exists in the map already
		if (map.containsKey(PROCESSED_BODY_NAME)) {
			// get the existing value and append the new value  
			String newValue = map.get(PROCESSED_BODY_NAME);
			newValue+=emailBody + FILE_SEPARATOR;
			map.put(PROCESSED_BODY_NAME, newValue);
		}
		else
			map.put(PROCESSED_BODY_NAME, emailBody + FILE_SEPARATOR);
	}
	
