	@Test 
	public void testIPv6Packet_1() {
		intializeTest();
		
		String ipPacket = createIPv6Packet_1();
		
		byte [] bytes = javax.xml.bind.DatatypeConverter.parseHexBinary(ipPacket);
		DataInputStream dis = new DataInputStream(new ByteArrayInputStream(bytes));
		
		NetworkPacket packet = this.parseNetworkPacket("IPv6", dis);
		
		assertNotNull(packet);
		assertTrue(packet instanceof IPv6Packet);
		
		IPv6Packet ipv6Packet = (IPv6Packet) packet;
		assertTrue(ipv6Packet.getVersion() == 6);
		assertTrue(ipv6Packet.getTrafficClass().getDSCP() == 0);
		assertTrue(ipv6Packet.getTrafficClass().getECN() == ECN.NOT_ECT);
		assertTrue(ipv6Packet.getFlowLabel() == 0);
		assertTrue(ipv6Packet.getPayloadLength() == 260);
		assertTrue(ipv6Packet.getProtocol() == IpProtocol.TCP);
		assertTrue(ipv6Packet.getHopLimit() == 64);
		assertTrue(ipv6Packet.getSource().equals("2001:06f8:102d:0000:02d0:09ff:fee3:e8de"));
		assertTrue(ipv6Packet.getDestination().equals("2001:06f8:0900:07c0:0000:0000:0000:0002"));
		
	}
	
	protected String createIPv4Packet_1() {
		return "45000047000040003e11462bac1c0a06c03a801e"; // Upper Protocol is UDP
	}
	
	protected String createIPv4Packet_2() {
		return "4500009b000040003d1146d7c03a801eac1c0a06"; // Upper Protocol is UDP
	}
	
	protected String createIPv4Packet_3() {
		return "450004ac1154400080066ba8ac10108bac1010a4"; // Upper Protocol is TCP
	}
	
	protected String createIPv6Packet_1() { 
		return "6000000001040640200106f8102d000002d009fffee3e8de200106f8090007c00000000000000002"; // Upper Protocol is TCP
	}
	
	private void intializeTest() {
		DataLinkPacketParser dataLinkParser = EthernetFrameParser.newInstance();
		addDataLinkLayerParser(dataLinkParser.getSupportedProtocol(), dataLinkParser);
		
		NetworkPacketParser networkParser = IPv4PacketParser.newInstance();
		addNetworkLayerParser(networkParser.getSupportedProtocol(), networkParser);
		networkParser = IPv6PacketParser.newInstance();
		addNetworkLayerParser(networkParser.getSupportedProtocol(), networkParser);
	}
	
