	public static DNSPCAPParser createCustomDNSParser() throws Exception {
		
		String basedir = System.getProperty("basedir");
		
		if (basedir == null) {
			System.err.println("Need to set basedir in system properties at startup time");
		 }
		
		SimpleConfigurableTranslator translator = new SimpleConfigurableTranslator();
		translator.setModelName("dns");
		translator.setModelVersion("1.0");
		translator.setInputFormatName("testdns");
		translator.loadDataModel(basedir + "/src/test/resources/models");
		
		DNSPCAPParser parser = new DNSPCAPParser();
		parser.setDefaultSource("JUNIT");
		parser.setDefaultAccessLabel("UNCLASSIFIED");
		parser.setTranslator(translator);
		
		Properties emptyProps = new Properties();
		parser.setStreamProperties(emptyProps);
		
		return parser;
		
	}

