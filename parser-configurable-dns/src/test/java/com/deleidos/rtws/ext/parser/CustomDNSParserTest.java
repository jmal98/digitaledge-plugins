	public void testInvalidDnsQueryRecordTypeAOverUDP() throws Exception {
		
		String dnsPacket = createRecordTypeAQueryPacket_Invalid();	
		DNSPCAPParser dnsParser = createCustomDNSParser();
		
		dnsParser.initialize();
		dnsParser.setInputStream(new ByteArrayInputStream(dnsPacket.getBytes()));
		
		JSONObject jsonObj = dnsParser.parse();
		assertNull(jsonObj); // Default is to discard malformed packets, so this should be null
		
		dnsParser.setDiscardMalformedPacket(false);
		dnsParser.setInputStream(new ByteArrayInputStream(dnsPacket.getBytes()));
		jsonObj = dnsParser.parse();
		assertNotNull(jsonObj);
	}
	
