	@Test
	public void testDigitalEdgePacket_1() throws Exception {
		initializeThis();
		
		String deData = "0000013ABD8EC0E40000";
		
		byte [] bytes = javax.xml.bind.DatatypeConverter.parseHexBinary(deData);
		DataInputStream dis = new DataInputStream(new ByteArrayInputStream(bytes));
		
		ApplicationPacket packet = this.parseApplicationPacket("DE", bytes, dis);
		
		assertNotNull(packet);
		assertTrue(packet instanceof DigitalEdgePacket);
		
		DigitalEdgePacket dePacket = (DigitalEdgePacket) packet;
		assertTrue(dePacket.getTimestamp() == 1351799980260L); // Thu Nov 01 15:59:40 EDT 2012
		assertTrue(dePacket.getQueueFull() == 0);
		assertTrue(dePacket.getStorageOverflow() == 0);
		
	}

