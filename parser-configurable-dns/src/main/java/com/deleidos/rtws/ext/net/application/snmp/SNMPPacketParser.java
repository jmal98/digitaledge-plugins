package com.deleidos.rtws.ext.net.application.snmp;

import java.io.DataInputStream;
import java.io.IOException;
import java.util.ArrayList;

import com.deleidos.rtws.ext.net.application.common.ApplicationPacketParser;
import com.deleidos.rtws.ext.net.application.snmp.model.AbstractPDU;
import com.deleidos.rtws.ext.net.application.snmp.model.BulkPDU;
import com.deleidos.rtws.ext.net.application.snmp.model.GeneralPDU;
import com.deleidos.rtws.ext.net.application.snmp.model.ObjectIdentifier;
import com.deleidos.rtws.ext.net.application.snmp.model.SNMPPacket;
import com.deleidos.rtws.ext.net.application.snmp.model.TrapPDU;
import com.deleidos.rtws.ext.net.application.snmp.model.VariableBinding;
import com.deleidos.rtws.ext.net.application.snmp.model.AbstractPDU.PDUType;
import com.deleidos.rtws.ext.net.application.snmp.model.GeneralPDU.PDUErrorStatus;
import com.deleidos.rtws.ext.net.application.snmp.model.SNMPPacket.SNMPVersion;
import com.deleidos.rtws.ext.net.application.snmp.model.TrapPDU.TrapPDUGenericTrap;
import com.deleidos.rtws.ext.net.application.snmp.util.BEREncodedData;
import com.deleidos.rtws.ext.net.application.snmp.util.BERSNMPTags;
import com.deleidos.rtws.ext.net.common.NotSupportedException;
import com.deleidos.rtws.ext.net.transport.udp.model.UDPPortProtocol;

// See RFC 1157 for definition of SNMPv1
// See RFC 3416 for definition of SNMPv2
public class SNMPPacketParser implements ApplicationPacketParser {

	public static SNMPPacketParser newInstance() {
		return new SNMPPacketParser();
	}
	
	private SNMPPacketParser() {
		// Not instantiable
	}

	@Override
	public String getSupportedProtocol() {
		return UDPPortProtocol.SNMP.toString();
	}
	
	@Override
	public SNMPPacket parse(byte[] bytes, DataInputStream dis) throws Exception {
		SNMPPacket packet = new SNMPPacket();
				
		// SNMP uses Abstract Syntax Notation One(ASN.1) notation encoded
		// with Basic Encoding Rules(BER).  ASN.1 is a notation
		// for describing rules and structures.  BER is a method of
		// encoding using the following style:
		// 		Type | Length | Data
		// That is, each part of a structure follows this format, so you must
		// always check the type and length before trying to read the next part.
		int typeIdentifier = dis.readUnsignedByte();
		packet.setPacketSize(BEREncodedData.calculateLength(dis));
		
		// Make sure the packet is of type SEQUENCE
		if(typeIdentifier == BERSNMPTags.SEQUENCE) {
			SNMPVersion version = SNMPVersion.fromValue(parseInteger(dis));
			if(version != null) {
				if( version != SNMPVersion.SNMPv1 &&
					version != SNMPVersion.SNMPv2c) {
					throw new NotSupportedException(
							String.format("%s not supported. Only %s and %s are supported.",
											version.toString(),
											SNMPPacket.SNMPVersion.SNMPv1.toString(),
											SNMPPacket.SNMPVersion.SNMPv2c.toString()));
				}
				
				packet.setVersion(version);
			}
			
			String community = parseString(dis);
			if(community != null) {
				packet.setCommunity(community);
			}
			
			AbstractPDU pdu = parsePdu(dis);
			if(pdu != null) {
				packet.setPdu(pdu);
			}
		}
	
		return packet;
	}
	
	private AbstractPDU parsePdu(DataInputStream dis) throws IOException {
		AbstractPDU pdu = null;
		BEREncodedData pduField = new BEREncodedData();
		pduField.parseField(dis);
		
		// Verify that the pdu field has a valid tag
		if( pduField.getTag() == BERSNMPTags.GET_REQUEST_PDU ||
			pduField.getTag() == BERSNMPTags.GET_NEXT_REQUEST_PDU ||
			pduField.getTag() == BERSNMPTags.GET_RESPONSE_PDU ||
			pduField.getTag() == BERSNMPTags.RESPONSE_PDU ||
			pduField.getTag() == BERSNMPTags.SET_REQUEST_PDU ||
			pduField.getTag() == BERSNMPTags.INFORM_REQUEST_PDU ||
			pduField.getTag() == BERSNMPTags.SNMPv2_TRAP_PDU ||
			pduField.getTag() == BERSNMPTags.REPORT_PDU
				) {
			pdu = parseGeneralPdu(pduField.asDataInputStream());
		}
		else if(pduField.getTag() == BERSNMPTags.GET_BULK_REQUEST_PDU) {
			pdu = parseBulkPdu(pduField.asDataInputStream());
		}
		else if(pduField.getTag() == BERSNMPTags.TRAP_PDU) {
			pdu = parseTrapPdu(pduField.asDataInputStream());
		}
		
		// Set the PDU type.  The type values are 160 less than
		// their corresponding BER SNMP Tag value.
		pdu.setType(PDUType.fromValue(pduField.getTag()-160));
		
		return pdu;
	}
	
	private GeneralPDU parseGeneralPdu(DataInputStream dis) throws IOException {
		GeneralPDU gPdu = new GeneralPDU();
		
		int requestId = parseInteger(dis);
		if(requestId >= 0) {
			gPdu.setRequestId(requestId);
		}
		
		PDUErrorStatus errorStatus = PDUErrorStatus.fromValue(parseInteger(dis));
		if(errorStatus != null) {
			gPdu.setErrorStatus(errorStatus);
		}
		
		int errorIndex = parseInteger(dis);
		if(errorIndex >= 0) {
			gPdu.setErrorIndex(errorIndex);
		}
		
		ArrayList<VariableBinding> variableBindings = parseVariableBindings(dis);
		if(variableBindings != null) {
			gPdu.setVariableBindings(variableBindings);
		}
		
		return gPdu;
	}
	
	private BulkPDU parseBulkPdu(DataInputStream dis) throws IOException {
		BulkPDU bPdu = new BulkPDU();
		
		int requestId = parseInteger(dis);
		if(requestId >= 0) {
			bPdu.setRequestId(requestId);
		}
		
		int nonRepeaters = parseInteger(dis);
		if(nonRepeaters >= 0) {
			bPdu.setNonRepeaters(nonRepeaters);
		}
		
		int maxRepetitions = parseInteger(dis);
		if(maxRepetitions >= 0) {
			bPdu.setMaxRepetitions(maxRepetitions);
		}
		
		ArrayList<VariableBinding> variableBindings = parseVariableBindings(dis);
		if(variableBindings != null) {
			bPdu.setVariableBindings(variableBindings);
		}
		
		return bPdu;
	}
	
	private TrapPDU parseTrapPdu(DataInputStream dis) throws IOException {
		TrapPDU tPdu = new TrapPDU();
		
		ObjectIdentifier oid = parseObjectIdentifier(dis);
		if(oid != null) {
			tPdu.setEnterprise(oid.toString());
		}
		
		String agentAddress = parseIpAddress(dis);
		if(agentAddress != null) {
			tPdu.setAgentAddress(agentAddress);
		}
		
		TrapPDUGenericTrap genericTrap = TrapPDUGenericTrap.fromValue(parseInteger(dis));
		if(genericTrap != null) {
			tPdu.setGenericTrap(genericTrap);
		}
		
		int specificTrap = parseInteger(dis);
		if(specificTrap >= 0) {
			tPdu.setSpecificTrap(specificTrap);
		}
		
		int timeStamp = parseTimeTicks(dis);
		if(timeStamp >= 0) {
			tPdu.setTimeStamp(timeStamp);
		}
		
		ArrayList<VariableBinding> variableBindings = parseVariableBindings(dis);
		if(variableBindings != null) {
			tPdu.setVariableBindings(variableBindings);
		}
		
		return tPdu;
	}
	
	private int parseInteger(DataInputStream dis) throws IOException {
		int integer = -1;
		BEREncodedData integerField = new BEREncodedData();
		integerField.parseField(dis);
		
		// Verify that the integer field tag is of type INTEGER
		if(integerField.getTag() == BERSNMPTags.INTEGER) {
			integer = integerField.asInteger();
		}
		
		return integer;
	}
	
	private String parseString(DataInputStream dis) throws IOException {
		String string = null;
		BEREncodedData stringField = new BEREncodedData();
		stringField.parseField(dis);
		
		// Verify that the string field tag is of type OCTET STRING
		if(stringField.getTag() == BERSNMPTags.OCTECT_STRING) {
			string = stringField.asString();
		}

		return string;
	}
	
	private ObjectIdentifier parseObjectIdentifier(DataInputStream dis) throws IOException {
		BEREncodedData oidField = new BEREncodedData();
		oidField.parseField(dis);
		
		// Verify that the oid field tag is of type OBJECT IDENTIFIER
		if(oidField.getTag() == BERSNMPTags.OBJECT_IDENTIFIER) {
			return oidField.asObjectIdentifier();
		}
		else {
			return null;
		}
	}
	
	private String parseIpAddress(DataInputStream dis) throws IOException {
		String ipAddress = null;
		BEREncodedData ipAddressField = new BEREncodedData();
		ipAddressField.parseField(dis);
		
		// Verify that the string field tag is of type OCTET STRING
		if(ipAddressField.getTag() == BERSNMPTags.IP_ADDRESS) {
			ipAddress = ipAddressField.asIpAddress();
		}

		return ipAddress;
	}
	
	private int parseTimeTicks(DataInputStream dis) throws IOException {
		int timeTicks = -1;
		BEREncodedData timeTicksField = new BEREncodedData();
		timeTicksField.parseField(dis);
		
		// Verify that the string field tag is of type OCTET STRING
		if(timeTicksField.getTag() == BERSNMPTags.TIME_TICKS) {
			timeTicks = timeTicksField.asTimeStamp();
		}

		return timeTicks;
	}
	
	private ArrayList<VariableBinding> parseVariableBindings(DataInputStream dis)
		throws IOException {
		ArrayList<VariableBinding> variableBindings = null;
		BEREncodedData bindingsField = new BEREncodedData();
		bindingsField.parseField(dis);
		
		// Verify that the bindings field tag is of type SEQUENCE
		if(bindingsField.getTag() == BERSNMPTags.SEQUENCE) {
			variableBindings = new ArrayList<VariableBinding>();
			BEREncodedData bindingField = new BEREncodedData();
			DataInputStream bindingsDis = bindingsField.asDataInputStream();
			
			int bytesRead = 0;
			while( bytesRead < bindingsField.getDataLength() ) {
				bindingField.parseField(bindingsDis);
				VariableBinding binding = null;
				
				// Verify that the binding field tag is of type SEQUENCE
				if(bindingField.getTag() == BERSNMPTags.SEQUENCE) {
					binding = parseVariableBinding(bindingField.asDataInputStream());
					variableBindings.add(binding);					
				}
				
				// Increment how many bytes have been read to keep
				// track of how close to the end of the stream we are
				bytesRead += bindingField.getTotalLength();
			}
		}
		
		return variableBindings;
	}
	
	private VariableBinding parseVariableBinding(DataInputStream dis) throws IOException {
		VariableBinding binding = new VariableBinding();
		BEREncodedData field = new BEREncodedData();
		
		// Get the object identifier
		field.parseField(dis);
		String oid = null;
		if(field.getTag() == BERSNMPTags.OBJECT_IDENTIFIER) {
			oid = field.asObjectIdentifier().toString();
		}
		binding.setObjectName(oid);
		
		// Get the value of the binding
		field.parseField(dis);
		String value = null;
		switch(field.getTag()) {
			case BERSNMPTags.INTEGER :
				value = Integer.toString(field.asInteger());
				break;
			case BERSNMPTags.OCTECT_STRING :
				value = field.asString();
				break;
			case BERSNMPTags.OBJECT_IDENTIFIER :
				value = field.asObjectIdentifier().toString();
				break;
			case BERSNMPTags.IP_ADDRESS :
				value = field.asIpAddress();
				break;
			case BERSNMPTags.COUNTER_32 :
				value = Integer.toString(field.asCounter32());
				break;
			case BERSNMPTags.TIME_TICKS :
				value = Integer.toString(field.asTimeStamp());
				break;
			case BERSNMPTags.OPAQUE :
				value = field.asOpaque();
				break;
			case BERSNMPTags.COUNTER_64 :
				value = Long.toString(field.asCounter64());
				break;
			case BERSNMPTags.UNSIGNED_32 :
				value = Integer.toString(field.asUnsigned32());
				break;
			default :
				break;
		}
		
		binding.setValue(value);
		
		
		return binding;
	}
}
