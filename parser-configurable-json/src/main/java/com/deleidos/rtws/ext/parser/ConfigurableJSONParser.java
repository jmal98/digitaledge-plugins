package com.deleidos.rtws.ext.parser;

import java.text.ParseException;
import java.util.HashMap;
import java.util.Iterator;

import net.sf.json.JSONArray;
import net.sf.json.JSONObject;

import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.exception.InitializationException;
import com.deleidos.rtws.commons.util.Initializable;
import com.deleidos.rtws.core.framework.Description;
import com.deleidos.rtws.core.framework.UserConfigured;
import com.deleidos.rtws.core.framework.parser.JSONParser;
import com.deleidos.rtws.core.framework.parser.ParsePipelineException;
import com.deleidos.rtws.core.framework.translator.AbstractConfigurableTranslator;
import com.deleidos.rtws.core.metrics.ParseErrorCounter;
import com.deleidos.rtws.core.util.StandardHeader;

/**
 * Configurable JSON parser. Allows parsing of a JSON file into a set of fields
 * that can be used by the configurable translator.
 */
@Description("This configurable parser is used to parse JSON files into a set of fields to be used by the translator.")
public class ConfigurableJSONParser extends JSONParser implements Initializable {

	/** For logging parsing problems. */
	private static final Logger log = Logger
			.getLogger(ConfigurableJSONParser.class);

	/** For translating to IRAD format. */
	private AbstractConfigurableTranslator translator;

	/** Source used if none is specified. */
	private String defaultSource;

	/** Access Label used if none is specified. */
	private String defaultAccessLabel;
	
	private String defaultUUID = null;

	/** Represents the open array character. */
	private final String openArrayCharacter = "[";

	/** Represents the close array character. */
	private final String closeArrayCharacter = "]";

	/** Represents the JSON object separator character. */
	private final String objectSeperator = ".";

	/**
	 * Constructor.
	 */

	public ConfigurableJSONParser() {
		super("UTF-8");
	}

	/**
	 * Set the translator that will convert the parsed fields to a data model.
	 */
	public void setTranslator(AbstractConfigurableTranslator translator) {
		this.translator = translator;
	}

	/**
	 * @return the translator
	 */
	public AbstractConfigurableTranslator getTranslator() {
		return this.translator;
	}

	/**
	 * Closes the translator once the parser is finish with it.
	 */
	public void dispose() {
		translator.dispose();
	}

	/**
	 * Initialize the translator for the parser.
	 */
	public void initialize() throws InitializationException {
		translator.initialize();
	}

	public void parseHeaders() {
		// No Implementation Necessary
	}

	/**
	 * Parse and return the next JSON record, or null if all done or error.
	 * 
	 * @return returns a JSONObject with the parsed data.
	 */
	public JSONObject parse() throws ParsePipelineException {

		JSONObject parsedData = null;

		parsedData = super.parse();
		if (parsedData != null) {
			if (info == null) {
				log.error("Configurable JSON Parser: No Default Properties Set.");
				throw new ParsePipelineException("Configurable JSON Parser: No Default Properties Set.");
			}

			String streamAccessLabel = info
					.getProperty(StandardHeader.ACCESS_LABEL_KEY);
			String accessLabel = (streamAccessLabel == null) ? defaultAccessLabel
					: streamAccessLabel;

			String streamSource = info.getProperty(StandardHeader.SOURCE_KEY);
			String source = (streamSource == null) ? defaultSource
					: streamSource;
			
			String streamUUID = this.info.getProperty("uuidOverride");
			String uuid = streamUUID == null ? this.defaultUUID : parsedData.get(streamUUID).toString();

			HashMap<String, String> map = new HashMap<String, String>();

			loadJSONFields(map, parsedData, null, null);
			try{
				if (uuid != null) {
					parsedData = translator.recordTranslation(map, source, accessLabel, uuid);
				} else {
					parsedData = translator.recordTranslation(map, source, accessLabel);
				}
				
			}catch(ParseException e){
				log.error("Error parsing record in ConfigurableJSONParser: " + e.getMessage());
				ParseErrorCounter.instance.increment();
				throw new ParsePipelineException(e.getMessage(), e);
			}
			return parsedData;
		}
		return parsedData;
	}

	/**
	 * This method will be called recursively to process all the variables. It
	 * will process both the objects and the arrays.
	 * 
	 * 
	 * @param map
	 *            HashMap that contains the data that will be used to build the
	 *            IRAD JSON file.
	 * @param parsedData
	 *            An JSON Object taken from the input file.
	 * @param levelName
	 *            The JSON path for the variables.
	 * @param indexName
	 *            Used for JSON Array to specify an index for the variables.
	 */
	private void loadJSONFields(HashMap<String, String> map,
			JSONObject parsedData, String levelName, Integer indexName) {
		Iterator<String> keysIterator = parsedData.keys();
		String keyName = null;

		if ((levelName != null) && (indexName != null)) {
			levelName += openArrayCharacter + indexName + closeArrayCharacter;
		}

		while (keysIterator.hasNext()) {
			String key = keysIterator.next();
			Object object = parsedData.get(key);

			if (levelName != null) {
				keyName = levelName + objectSeperator + key;
			} else {
				if (indexName != null) {
					keyName = key + openArrayCharacter + indexName
							+ closeArrayCharacter;
				} else {
					keyName = key;
				}
			}

			if (object instanceof JSONObject) {
				JSONObject jSONObject = parsedData.getJSONObject(key);
				loadJSONFields(map, jSONObject, keyName, null);
				continue;
			} else if (object instanceof JSONArray) {
				JSONArray jSONArray = parsedData.getJSONArray(key);

				if (!jSONArray.isEmpty()) {

					Object arrayObject = jSONArray.get(0);

					if (arrayObject instanceof JSONObject) {
						for (int index = 0; index < jSONArray.size(); index++) {
							Integer indexNumber = new Integer(index);
							JSONObject jObject = jSONArray.getJSONObject(index);
							loadJSONFields(map, jObject, keyName, indexNumber);
						}
					} else {
						for (int index = 0; index < jSONArray.size(); index++) {
							Integer indexNumber = new Integer(index);
							Object otherObject = jSONArray.get(index);
							String value = parsedData.getString(key);
							String indexedKeyName = keyName
									+ openArrayCharacter + indexName
									+ closeArrayCharacter;
							map.put(indexedKeyName, value);
						}
					}
				} else {
					if (log.isDebugEnabled())
						log.debug("Ignoring empty array: " + key);
				}
			}

			String value = parsedData.getString(key);
			map.put(keyName, value);
		}
	}

	/**
	 * Set the default access label.
	 */
	@UserConfigured(value = "UNCLASSIFIED", description = "The default access label to include with the data.")
	public void setDefaultAccessLabel(String defaultAccessLabel) {
		this.defaultAccessLabel = defaultAccessLabel;
	}

	/**
	 * @return the default access label
	 */
	public String getDefaultAccessLabel() {
		return defaultAccessLabel;
	}

	/**
	 * Set the default source.
	 */
	@UserConfigured(value = "UNKNOWN", description = "The string describing the source of the data.")
	public void setDefaultSource(String defaultSource) {
		this.defaultSource = defaultSource;
	}

	/**
	 * @return the default source
	 */
	@UserConfigured(value = "UNKNOWN")
	public String getDefaultSource() {
		return defaultSource;
	}
}
