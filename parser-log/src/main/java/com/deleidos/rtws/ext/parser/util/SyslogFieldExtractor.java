package com.deleidos.rtws.ext.parser.util;

import java.util.HashMap;
import java.util.Iterator;
import java.util.Map;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

import org.apache.log4j.Logger;

import com.deleidos.rtws.ext.parser.SyslogMessageFormatFields;

import net.sf.json.JSONObject;

/**
 * Extracts fields corresponding to RFC5424
 * 
 * @see https://tools.ietf.org/html/rfc5424#page-8
 * 
 *
 */
public class SyslogFieldExtractor {

	private static final Logger logger = Logger.getLogger(SyslogFieldExtractor.class);

	private static Pattern procidPattern = Pattern.compile("(\\[\\d+\\])");

	public static final String NILVALUE = "-";

	public static Map<String, String> extract(String syslogMsg) {
		Map<String, String> fields = new HashMap<String, String>();

		fields.put("PROCESSED_AT", Long.toString(System.currentTimeMillis()));

		if (syslogMsg == null)
			return fields;

		if (syslogMsg.length() < 1)
			return fields;

		try {

			String[] parts = syslogMsg.split("\\s");
			int z = 0;
			for (String p : parts) {
				if (logger.isDebugEnabled()) {
					logger.debug(String.format("parts[%s]: %s", z, p));
				}
				z++;
			}

			if (!parts[2].equals(NILVALUE))
				fields.put(SyslogMessageFormatFields.HOSTNAME.toString(), parts[2]);

			String appName = parts[3];
			if (appName.contains("[")) {

				Matcher matcher = procidPattern.matcher(appName);
				if (matcher.find()) {
					for (int i = 0; i < matcher.groupCount(); i++) {
						String procId = matcher.group(i + 1);
						if (!procId.equals(NILVALUE))
							fields.put(SyslogMessageFormatFields.PROCID.toString(), procId.replaceAll("[\\[\\]]", ""));
					}

				}
				fields.put(SyslogMessageFormatFields.APP_NAME.toString(), appName.substring(0, appName.indexOf('[')));

			} else if (appName.contains(":")) {
				fields.put(SyslogMessageFormatFields.APP_NAME.toString(), appName.substring(0, appName.indexOf(':')));
			} else {
				if (!appName.equals(NILVALUE))
					fields.put(SyslogMessageFormatFields.APP_NAME.toString(), appName);
			}
		} catch (ArrayIndexOutOfBoundsException e) {
			logger.warn(String.format("Unable to extract one or more fields."));
		} catch (StringIndexOutOfBoundsException e) {
			logger.warn(String.format("Unable to extract one or more fields."));
		}

		return fields;
	}

	public static Map<String, String> extractFromJson(String line) {
		Map<String, String> recordMap = new HashMap<String, String>();

		if (line == null)
			return recordMap;

		JSONObject json = JSONObject.fromObject(line);
		@SuppressWarnings("unchecked")
		Iterator<String> iterator = json.keys();
		while (iterator.hasNext()) {
			String key = iterator.next();
			recordMap.put(key, json.getString(key));
		}

		// strip trailing ':' and pid '[123]' from tag if exists
		if (recordMap.containsKey("syslog-tag")) {

			if (recordMap.get("syslog-tag").contains("[")) {
				String appName = recordMap.get("syslog-tag").substring(0, recordMap.get("syslog-tag").indexOf('['));

				Matcher matcher = procidPattern.matcher(recordMap.get("syslog-tag"));
				if (matcher.find()) {
					for (int i = 0; i < matcher.groupCount(); i++) {
						String procId = matcher.group(i + 1);
						if (!procId.equals(NILVALUE))
							recordMap.put(SyslogMessageFormatFields.PROCID.toString(),
									procId.replaceAll("[\\[\\]]", ""));
					}

				}

				if (appName.contains(":")) {
					recordMap.put(SyslogMessageFormatFields.APP_NAME.toString(),
							appName.substring(0, appName.length() - 1));
					recordMap.put("syslog-tag", appName.substring(0, appName.length() - 1));
				} else {
					recordMap.put(SyslogMessageFormatFields.APP_NAME.toString(), appName);
					recordMap.put("syslog-tag", appName);
				}
			} else {
				recordMap.put("syslog-tag",
						json.getString("syslog-tag").endsWith(":")
								? json.getString("syslog-tag").substring(0, json.getString("syslog-tag").length() - 1)
								: json.getString("syslog-tag"));
			}

			recordMap.put(SyslogMessageFormatFields.APP_NAME.toString(), recordMap.get("syslog-tag"));
		}

		if (recordMap.containsKey("tag")) {

			if (recordMap.get("tag").contains("[")) {
				String appName = recordMap.get("tag").substring(0, recordMap.get("tag").indexOf('['));

				Matcher matcher = procidPattern.matcher(recordMap.get("tag"));
				if (matcher.find()) {
					for (int i = 0; i < matcher.groupCount(); i++) {
						String procId = matcher.group(i + 1);
						if (!procId.equals(NILVALUE))
							recordMap.put(SyslogMessageFormatFields.PROCID.toString(),
									procId.replaceAll("[\\[\\]]", ""));
					}

				}

				if (appName.contains(":")) {
					recordMap.put(SyslogMessageFormatFields.APP_NAME.toString(),
							appName.substring(0, appName.length() - 1));
					recordMap.put("tag", appName.substring(0, appName.length() - 1));
				} else {
					recordMap.put(SyslogMessageFormatFields.APP_NAME.toString(), appName);
					recordMap.put("tag", appName);
				}
			} else {
				recordMap.put("tag",
						json.getString("tag").endsWith(":")
								? json.getString("tag").substring(0, json.getString("tag").length() - 1)
								: json.getString("tag"));
			}

			recordMap.put(SyslogMessageFormatFields.APP_NAME.toString(), recordMap.get("syslog-tag"));
		}

		if (recordMap.containsKey("host")) {
			recordMap.put(SyslogMessageFormatFields.HOSTNAME.toString(), recordMap.get("host"));
		}
		return recordMap;
	}
}
